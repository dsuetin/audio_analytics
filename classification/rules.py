def score(hist: dict) -> dict:
    buy = ret = svc = 0

    for w, c in hist.items():
        if w in BUY_KEYWORDS:
            buy += c
        elif w in RETURN_KEYWORDS:
            ret += c
        elif w in SERVICE_KEYWORDS:
            svc += c

    return {"buy": buy, "return": ret, "service": svc}


def best_label(scores: dict):
    label = max(scores, key=scores.get)
    return label, scores[label]